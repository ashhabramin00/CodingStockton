package week2;

public class Access {
    // Scope in JAVA
    // public - accessible anywhere
    // private - accessible only in the class

    private String cat1 = "Appa";
    private String cat2 = "Momo";
    String cat3 = "Korra";

    // Getter and Setter Methods
    // Getter Methods - Get the value of a private variable
    // Setter Methods - Set the value of a private variable

    // Getter Method
    public String getCat1() {
        return cat1;
    }

    // Setter Method
    public void setCat1(String x) {
        cat1 = x;
    }
    
    


    
    
}
